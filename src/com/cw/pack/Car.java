public class Car
{
	private int id;
	private String model;
	private String name;
	private int lengh;
	private int width;
	private int high;

	/**
	 * @return the id
	 */
	public int getId()
	{
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id)
	{
		this.id = id;
	}

	/**
	 * @return the model
	 */
	public String getModel()
	{
		return model;
	}

	/**
	 * @param model the model to set
	 */
	public void setModel(String model)
	{
		this.model = model;
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}

	/**
	 * @return the lengh
	 */
	public int getLengh()
	{
		return lengh;
	}

	/**
	 * @param lengh the lengh to set
	 */
	public void setLengh(int lengh)
	{
		this.lengh = lengh;
	}

	/**
	 * @return the width
	 */
	public int getWidth()
	{
		return width;
	}

	/**
	 * @param width the width to set
	 */
	public void setWidth(int width)
	{
		this.width = width;
	}

	/**
	 * @return the high
	 */
	public int getHigh()
	{
		return high;
	}

	/**
	 * @param high the high to set
	 */
	public void setHigh(int high)
	{
		this.high = high;
	}

}
